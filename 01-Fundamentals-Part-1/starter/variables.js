// let keyword 
// We use let keyword when we will reasign/mutate the value later
// And when we want a variable at some point but "wihtout" any value
// Of course i know that will be the "undefined" type
let firstname = "Rubens";

firstname = "Ruban";
console.log(`let: ${firstname}`)

let something;
console.log(`let: ${something}`)

// const keyword
// We use const when we cannot mutate the value 
